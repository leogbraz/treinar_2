package br.com.treinar.java_basico.estudo.exercicio;

public class TesteIf2 {
	public static void main(String[] args) {
		int numero = 2;
		if (numero == 1) {
			System.out.println("Número igual a um");
		} else if (numero == 2) {
			System.out.println("Número igual a dois");
		} else if (numero == 3) {
			System.out.println("Número igual a três");
		} else {
			System.out.println("Número inexistente");
		}
	}
}
